package kr.mentalcare.project.model;

public class Work {
	int num;
	String file_name;
	String file_path;
	int cost;
	int start_period;
	int end_period;
	int admin_num;
	int dt_num;
	String name;
	String description;
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getDescription() {
		return description;
	}
	public void setDescription(String description) {
		this.description = description;
	}
	public int getNum() {
		return num;
	}
	public void setNum(int num) {
		this.num = num;
	}
	public String getFile_name() {
		return file_name;
	}
	public void setFile_name(String file_name) {
		this.file_name = file_name;
	}
	public String getFile_path() {
		return file_path;
	}
	public void setFile_path(String file_path) {
		this.file_path = file_path;
	}
	public int getCost() {
		return cost;
	}
	public void setCost(int cost) {
		this.cost = cost;
	}
	public int getStart_period() {
		return start_period;
	}
	public void setStart_period(int start_period) {
		this.start_period = start_period;
	}
	public int getEnd_period() {
		return end_period;
	}
	public void setEnd_period(int end_period) {
		this.end_period = end_period;
	}
	public int getAdmin_num() {
		return admin_num;
	}
	public void setAdmin_num(int admin_num) {
		this.admin_num = admin_num;
	}
	public int getDt_num() {
		return dt_num;
	}
	public void setDt_num(int dt_num) {
		this.dt_num = dt_num;
	}

	
}
