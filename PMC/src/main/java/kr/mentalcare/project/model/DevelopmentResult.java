package kr.mentalcare.project.model;

public class DevelopmentResult {
	int r_num;
	Integer w_num;
	String file_name;
	public int getR_num() {
		return r_num;
	}
	public void setR_num(int r_num) {
		this.r_num = r_num;
	}
	public Integer getW_num() {
		return w_num;
	}
	public void setW_num(Integer w_num) {
		this.w_num = w_num;
	}
	public String getFile_name() {
		return file_name;
	}
	public void setFile_name(String file_name) {
		this.file_name = file_name;
	}
	
}
